export function t(key: string, lang: LanguageCode = defaultLanguage): string {
  return translations[lang]?.[key] || translations[defaultLanguage][key] || key
}

export function tf(key: string, replacements: Record<string, string | number>, lang: LanguageCode = defaultLanguage): string {
  let text = t(key, lang)
  Object.entries(replacements).forEach(([k, v]) => {
    text = text.replace(`{${k}}`, String(v))
  })
  return text
}